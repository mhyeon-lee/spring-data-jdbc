CREATE TABLE PERSON (
ID BIGINT GENERATED BY DEFAULT AS IDENTITY ( START WITH 1 ) PRIMARY KEY,
FIRST_NAME VARCHAR(100),
LAST_NAME VARCHAR(100),
SSN VARCHAR(100),
AGE BIGINT,
BIRTH TIMESTAMP,
ADULT BIT,
ADDRESS VARCHAR(100),
ADDRESS_DETAIL VARCHAR(100),
COUNTRY VARCHAR(100),
STATE VARCHAR(100),
ZIP_CODE VARCHAR(100),
JOB VARCHAR(100),
COMPANY VARCHAR(100),
EMAIL VARCHAR(100)
);
